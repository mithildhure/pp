package square;

public class Star_Square {

	public static void main(String[] args) {
		// Print a Sqaure full of start
//		*****
//		*****
//		*****
//		*****
//		*****
		
		for (int i = 0; i < 5; i++) {
			for (int j = 0; j < 5; j++) {
				System.out.print("* ");
			}
			System.out.println();
		}
		
		
	}

}
